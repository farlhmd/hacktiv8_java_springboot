package src.sesi2;

public class OperatorPembanding {
    public static void main(String[] args) {
        int bilangan1 = 15, bilangan2 = 3;
        boolean hasil;
        
        hasil = bilangan1 > bilangan2;
        System.out.println(hasil);

        hasil = bilangan1 < bilangan2;
        System.out.println(hasil);

        hasil = bilangan1 >= bilangan2;
        System.out.println(hasil);
        
        hasil = bilangan1 <= bilangan2;
        System.out.println(hasil);

        hasil = bilangan1 == bilangan2;
        System.out.println(hasil);

        hasil = bilangan1 != bilangan2;
        System.out.println(hasil);
    }
}
